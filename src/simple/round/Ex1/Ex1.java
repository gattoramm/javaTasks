package simple.round.Ex1;

public class Ex1 {
    public static void main(String[] args) {
        System.out.println(Math.round(3.49));//3
        System.out.println(Math.round(3.50));//4
        System.out.println(Math.round(3.51));//4
        System.out.println(Math.round(-3.49));//-3
        System.out.println(Math.round(-3.50));//-3
        System.out.println(Math.round(-3.51));//-4
    }
}
